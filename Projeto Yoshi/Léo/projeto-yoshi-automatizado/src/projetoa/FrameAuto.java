    private void btnSimularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimularActionPerformed
        // TODO add your handling code here:
         Random sorteador = new Random();
         Integer limite = 100;
         Integer limiteR = 500;
         
        int delay = 2000;   // tempo de espera antes da 1ª execução da tarefa.
        int interval = 1000;  // intervalo no qual a tarefa será executada.
        
        Timer timer = new Timer();
        
        timer.scheduleAtFixedRate(new TimerTask() {
            
        public void run() {
         Integer CPU = sorteador.nextInt(limite)+1;
         lbCPU.setText(String.valueOf(CPU) + "%");
         pbCPU.setValue(CPU);
         
         Integer RAM = sorteador.nextInt(limite)+1;
         lbRAM.setText(String.valueOf(RAM) + "%");
         pbRAM.setValue(RAM);
         
         Integer HD = sorteador.nextInt(limiteR)+1;
         lbHD.setText(String.valueOf(HD) + "MB");
         pbHD.setValue(HD);
  }
}, delay, interval);
       
         
         
         
         
    
    }//GEN-LAST:event_btnSimularActionPerformed

